/**
 * Определить код региона по названию или коду.
 * @param {string} input - название региона или существующий код
 * @returns {string} - code (верхний регистр)
 */
export function guessRegionCode(input) {
  if (!input) return "";
  
  const normalized = input.toLowerCase()
    .replace(/[-\s]/g, "_")
    .replace(/ё/g, "e");
  
  // Проверяем справочник
  if (REGION_CODES[normalized]) {
    return REGION_CODES[normalized];
  }
  
  // Если ввели в верхнем регистре и это короче 6 символов — вероятно, уже код
  const upper = input.toUpperCase();
  if (input === upper && input.length <= 6 && input.length >= 2) {
    return upper;
  }
  
  // Fallback: первые 3 буквы (в верхнем регистре)
  return input.slice(0, 3).toUpperCase();
}

/**
 * Рекомендовать категорию программы по названию программы.
 * @param {string} title - название программы
 * @returns {string} - recommended category
 */
export function recommendCategory(title) {
  if (!title) return "контракт_вс"; // default
  
  const lower = title.toLowerCase();
  
  for (const [category, keywords] of Object.entries(CATEGORY_PATTERNS)) {
    if (keywords.some(kw => lower.includes(kw))) {
      return category;
    }
  }
  
  return "контракт_вс"; // fallback
}

